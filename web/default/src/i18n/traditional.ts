export function toTraditionalTaiwanese(value: string): string {
  return applyReplacements(
    applyReplacements(applyReplacements(value, PHRASE_REPLACEMENTS), WORD_REPLACEMENTS),
    CHAR_REPLACEMENTS,
  )
}

export function transformTraditionalTaiwanese<T>(value: T): T {
  if (typeof value === 'string') return toTraditionalTaiwanese(value) as T
  if (Array.isArray(value)) return value.map((item) => transformTraditionalTaiwanese(item)) as T
  if (value && typeof value === 'object') {
    const output: Record<string, JsonValue> = {}
    for (const [key, entry] of Object.entries(value as Record<string, JsonValue>)) {
      output[key] = transformTraditionalTaiwanese(entry)
    }
    return output as T
  }
  return value
}
